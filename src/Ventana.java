    private void jPanel1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MousePressed
        switch (opcion) {
            // Circulo
            case 1:

                // Esquina superior izq circulo
                circulo.x = evt.getX();
                circulo.y = evt.getY();
                // Centro del circulo
                xOrigen = circulo.x;
                yOrigen = circulo.y;
                break;
            // Rectangulo    
            case 2:
                // Esquina superior izq cuadrado
                xOrigen = evt.getX();
                yOrigen = evt.getY();
                rectangulo.x = xOrigen;
                rectangulo.y = yOrigen;
                break;
            // Linea
            case 3:
                // Punto inicio
                linea.x1 = evt.getX();
                linea.y1 = evt.getY();
                break;
            // Libre
            case 4:
                x1 = evt.getX();
                y1 = evt.getY();
                break;
            case 5:
                // Esquina superior izq elipse
                circulo.x = evt.getX();
                circulo.y = evt.getY();
                xOrigen = circulo.x;
                yOrigen = circulo.y;
                break;
            case 6:
                // Esquina superior izq rectangulo
                xOrigen = evt.getX();
                yOrigen = evt.getY();
                rectangulo.x = xOrigen;
                rectangulo.y = yOrigen;
                break;
        }
    }//GEN-LAST:event_jPanel1MousePressed

    private void jPanel1MouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseDragged

        g2.drawImage(buffer, 0, 0, null);
        switch (opcion) {
            case 1:
                double radio,
                 d1,
                 d2;
                d1 = evt.getX() - xOrigen;
                if (d1 < 0) {
                    d1 = xOrigen - evt.getX();
                }
                d2 = evt.getY() - yOrigen;
                if (d2 < 0) {
                    d2 = yOrigen - evt.getY();
                }
                radio = pow(d1, 2) + pow(d2, 2); // modulo vector
                if (radio < 0) {
                    radio = radio * -1;
                }
                radio = sqrt(radio);
                circulo.x = xOrigen - radio;
                circulo.y = yOrigen - radio;
                circulo.height = radio * 2;
                circulo.width = circulo.height;
                g2.draw(circulo);
                break;
            case 2:
                double distancia = xOrigen - evt.getX();
                if (distancia < 0) {
                    distancia = evt.getX() - xOrigen;
                }
                double yDistancia = yOrigen - evt.getY();
                if (yDistancia < 0) {
                    yDistancia = evt.getY() - yOrigen;
                }
                if (distancia < yDistancia) {
                    distancia = yDistancia;
                }
                rectangulo.x = xOrigen - distancia;
                rectangulo.y = yOrigen - distancia;
                rectangulo.width = distancia * 2;
                rectangulo.height = rectangulo.width;
                g2.draw(rectangulo);
                break;
            case 3:
                linea.x2 = evt.getX();
                linea.y2 = evt.getY();
                g2.draw(linea);
                break;
            case 4:
                Graphics2D g3 = (Graphics2D) buffer.getGraphics();
                x2 = evt.getX();
                y2 = evt.getY();
                if (x1 != x2 || y1 != y2) {
                    g4.drawLine(x1, y1, x2, y2);
                    x1 = x2;
                    y1 = y2;
                }
                break;
            case 5:
                if (evt.getX() > xOrigen) {
                    circulo.width = evt.getX() - circulo.x;
                } else {
                    circulo.x = evt.getX();
                    circulo.width = xOrigen - circulo.x;
                }
                if (evt.getY() > yOrigen) {
                    circulo.height = evt.getY() - yOrigen;
                } else {
                    circulo.y = evt.getY();
                    circulo.height = yOrigen - circulo.y;
                }
                g2.draw(circulo);
                break;
            case 6:
                dibujarRectangulo(evt, g2);
        }

    }//GEN-LAST:event_jPanel1MouseDragged

